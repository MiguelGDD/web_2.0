import Head from 'next/head'
import Layout from '../components/layout'
import About from '../components/About'
import Works from '../components/Works'
import Hero from '../components/Hero'
import { useEffect, useState } from 'react'  
import sanityClient from '../sanity'
import query from '../data/pageQuery'
import { useScroll, motion, useTransform } from "framer-motion";

export default function Home() {
  const [pageContent, setPageContent] = useState(false)

  const { scrollY } = useScroll();
  const y1 = useTransform(scrollY, [0, 300], [0, 200]);
  const y2 = useTransform(scrollY, [0, 300], [0, -250]);

  useEffect(() => {
    sanityClient.fetch(query).then((response)=> {
      setPageContent(response)
    })
  }, [])

  const { title, sub_title, image, about, projects } = pageContent

  return (
    <>
      <Head>
        <title>Miguel Guell | PORTFOLIO</title> 
        <meta name='description' content='Generated by create next app' />
      </Head>
      <Layout>
        <Hero
          y1={y1}
          title={title}
          subTitle={sub_title}
          image={image}
        />
        <motion.div
          style={{ y: y2, x: 0 }}
          transition={{ type: "spring", restDelta: 0.5 }}
          className='wave sm:px-1'>
          <About aboutData={about} />
          <Works worksData={projects} />
        </motion.div>
      </Layout>
    </>
  )
}
